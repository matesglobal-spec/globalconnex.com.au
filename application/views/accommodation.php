                    <h3>In Australia/New Zealand</h3>
                    <p>
                        International students can find the following main types of accommodation
                    </p>
                    <br>
                    <h4>Homestay</h4>
                    <p>
                        (Average price: $200 AUD per week)<br>

                        With this option you live with an Australian family and it’s probably the best way to discover the Australian culture and experience it’s amazing lifestyle. It is also an excellent way for the students who want to improve their English skills. This is one of the most popular options among students.
                    </p>
                    <br>
                    <h4>Hostel/Backpacker</h4>
                    <p>
                        (Average price: AUD$100-$180 per week)<br>

                        They provide budget oriented, sociable accommodation where guests can rent a bed in a dormitory and share a bathroom, lounge and kitchen. Rooms can be mixed or single-sex, although private rooms are also available.
                    </p>
                    <br>
                    <h4>Student Hostels</h4>
                    <p>
                        (Prices vary from AUD$100 to $300 per week per person depending on the type selected)<br>

                        The difference from the university residences is that these hostels are privately run and can vary in size from 5 rooms to 50 rooms or more!! The advantage of this option is that all rooms are furnished and kitchens are well-equipped.
                    </p>
                    <br>
                    <h4>On-Campus Student Accommodation</h4>
                    <p>
                        (Average price: $130 to $300 per week)<br>

                        On-campus residences are only available to university students and are limited because of the high demand. However, priority is given to international students. All rooms are furnished with well-equipped kitchens and you can choose from single or double rooms as well as serviced or not. It is most likely that you will share a bathroom and the kitchen.
                    </p>
                    <br>
                    <h4>Shared-Accommodation</h4>
                    <p>
                        (Average price: Between $90-$200 AUD per week)<br>

                        You can rent or share a room.<br>

                        In general this is the preferred option. It is cheap, you can lead the way of life you want and can choose who you want to live with!! You can share a house or a flat with Australians or other fellow students; your room may or may not be furnished.
                    </p>
                    <br>
                    <h4>Rental Properties</h4>
                    <p>
                        (Average price: $200-$600 AUD per week)<br>

                        If you are planning to stay for a long time in Australia and you are keen to have your own privacy, you can rent an apartment or house in your own name through a real estate agency. This is the most expensive option.
                    </p>
                    <br>
