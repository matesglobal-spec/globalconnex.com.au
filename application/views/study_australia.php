                                                <p class="mb-35">Australian institutions has become a host to international students for more than two decades. Australia is a proud education destination and a home to world-class institutions, campuses and academics. A multicultural and diverse study environment attracts students from all over the world. Australia has established an international reputation for excellence in all fields of education.</p>
                                                
                                                <p>What it offers to International Students</p>
                                                
                                                <p>
                                                    Global Recognition
                                                </p>
                                                
                                                <p>
                                                    NOOSR recognises overseas qualification - National Office of Overseas Skills Recognition (NOOSR) is an Australian Government organisation that assists Australian institutions to recognise qualifications from overseas. It also strives to promote the recognition of Australian qualifications internationally. The employers as well as leading educational institutes of the world recognize Australian qualifications
                                                </p>
                                                
                                                <p>
                                                    ESOS Framework
                                                </p>
                                                
                                                <p>
                                                    The Australian Government wants international students to have a safe, enjoyable and rewarding study experience. Australia is one of the few countries to enact laws to ensure quality education and consumer protection. These laws are known as the ESOS Framework and include the Education Services for Overseas Students (ESOS) Act 2000 and the National Code 2007.
                                                </p>

                                                <p>
                                                    The ESOS Act requires that all institutions that provide training to international students must be registered on the Commonwealth Register of Institutions and Courses for Overseas Students (CRICOS). CRICOS registration guarantees that the course and the education provider where you study meet high standards necessary for overseas students.
                                                </p>
                                                <p>Reputation of Australian Universities</p>
                                                
                                                <p>
                                                    Australia has 37 government-funded universities and two private universities, as well as a number of other specialist institutions that provide approved courses at the higher education level. The University of Sydney is Australia's oldest university, having been founded in 1850, followed by the University of Melbourne three years later. Other notable universities include those of the Group of Eight leading tertiary institutions, including the University of Adelaide (which boasts an association with five Nobel Laureates), the Australian National University located in the national capital of Canberra, Monash University and the University of New South Wales.
                                                </p>
                                                
                                                <p>
                                                    The OECD places Australia among the most expensive nations to attend university. There is a state-based system of vocational training, known as TAFE, and many trades conduct apprenticeships for training new trades people. Approximately 58 per cent of Australians aged from 25 to 64 have vocational or tertiary qualifications, and the tertiary graduation rate of 49 per cent is the highest among OECD countries. The ratio of international to local students in tertiary education in Australia is the highest in the OECD countries. All our programs and courses offered to international students are registered on CRICOS.
                                                </p>
